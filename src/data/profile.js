function clampLimit(limit, max = 8, fallback = 4) {
  if (!Number.isFinite(limit)) {
    return fallback;
  }

  return Math.min(Math.max(Math.round(limit), 1), max);
}

function normalize(value = "") {
  return value.toLowerCase().trim();
}

function scoreDocument(text, title, terms) {
  const haystack = normalize(`${title} ${text}`);
  return terms.reduce((score, term) => {
    if (!term) {
      return score;
    }

    if (haystack.includes(term)) {
      return score + (normalize(title).includes(term) ? 3 : 1);
    }

    return score;
  }, 0);
}

function getRoleKey(targetRole = "") {
  const normalizedRole = normalize(targetRole);

  if (normalizedRole.includes("director")) {
    return "director";
  }

  if (normalizedRole.includes("senior") || normalizedRole.includes("sr")) {
    return "senior_manager";
  }

  if (normalizedRole.includes("it")) {
    return "it_engineering";
  }

  return "manager";
}

export function searchProfileKnowledge(query, limit = 4) {
  const safeLimit = clampLimit(limit);
  const normalizedQuery = normalize(query);
  const terms = normalizedQuery.split(/[^a-z0-9+.#-]+/).filter(Boolean);

  const ranked = knowledgeDocuments
    .map((document) => ({
      ...document,
      score: scoreDocument(document.text, document.title, terms),
    }))
    .sort((left, right) => right.score - left.score);

  const filtered = ranked.filter((document) => document.score > 0);
  const source = filtered.length > 0 ? filtered : ranked;

  return source.slice(0, safeLimit).map((document) => ({
    section: document.section,
    title: document.title,
    excerpt: document.text.slice(0, 320),
  }));
}

export function getWorkHistory(company = "", limit = 5) {
  const safeLimit = clampLimit(limit, workExperience.length, workExperience.length);
  const normalizedCompany = normalize(company);
  const filtered = normalizedCompany
    ? workExperience.filter((item) =>
        normalize(`${item.company} ${item.role}`).includes(normalizedCompany)
      )
    : workExperience;

  return filtered.slice(0, safeLimit).map((item) => ({
    company: item.company,
    role: item.role,
    period: item.period,
    badge: item.badge,
    impact: item.impact,
    highlights: item.highlights,
  }));
}

export function getSkillInventory(focus = "", limit = 6) {
  const safeLimit = clampLimit(limit, 12, 6);
  const normalizedFocus = normalize(focus);

  const filteredGroups = normalizedFocus
    ? skillGroups.filter((group) =>
        normalize(`${group.title} ${group.skills.map((skill) => skill.name).join(" ")}`).includes(
          normalizedFocus
        )
      )
    : skillGroups;

  return filteredGroups.map((group) => ({
    title: group.title,
    skills: group.skills.slice(0, safeLimit).map((skill) => skill.name),
  }));
}

